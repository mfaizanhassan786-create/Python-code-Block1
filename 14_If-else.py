# 5 Very Simple If-Else Examples

# Example 1: Check if a number is greater than 10
# This is a very simple check.
num = 15
if num > 10:
    print("Number is greater than 10")
else:
    print("Number is not greater than 10")

# Example 2: Check if a password is correct
# Simple string comparison.
password = input("Enter password: ")
if password == "12345":
    print("Access Granted")
else:
    print("Access Denied")

# Example 3: Check if it is raining
# Simple boolean check.
is_raining = True
if is_raining:
    print("Take an umbrella")
else:
    print("No need for an umbrella")

# Example 4: Check if passing score
# Simple integer comparison.
score = int(input("Enter your score: "))
if score >= 50:
    print("You Passed")
else:
    print("You Failed")

# Example 5: Check Logic (True/False)
# Very basic True/False check.
value = True
if value:
    print("It is True")
else:
    print("It is False")
